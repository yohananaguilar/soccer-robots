void setup()
{
  pinMode(13, OUTPUT); // Definicao do pino 13 como saida
}

void loop()
{
  digitalWrite(13, HIGH); // Ativa a saida digital 13
  delay(1000); // Temporizacao de 1 s

  digitalWrite(13, LOW); // Desativa a saida digital 13
  delay(1000); // Temporizacao de 1 s
}

